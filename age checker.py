# This is a simple age checker script
age = int(input('Enter your age:'))  # Convert input to integer
if age < 18:
    print('Sorry, you are not eligible to vote')  # If the user's age is less than 18, it prints this
else:
    print('You are eligible to vote')  # If user's age is 18 or more, it prints this
# I hope you understand this code and how it works. I only used if and else statement because there is no other criteria that needs to be checked.